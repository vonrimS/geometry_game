import turtle

# create a canvas instance
myturtle = turtle.Turtle()

# go to a certain coordinate
myturtle.penup()
myturtle.goto(80, 215)

myturtle.pendown()
myturtle.forward(100)
myturtle.left(90)
myturtle.forward(100)
myturtle.left(90)
myturtle.forward(100)
myturtle.left(90)
myturtle.forward(100)

turtle.done()